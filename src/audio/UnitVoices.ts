const EASTER_EGG_PROBABILITY = 0.05;
const EASTER_EGG_ACTIONS: ReadonlySet<VoiceAction> = new Set(['select', 'idle', 'ready']);

/** Hook for tests: deterministic random source. */
export const __easterEggRng = { random: () => Math.random() };

// ---------------------------------------------------------------------------
// Preload tracking — avoid retrying files that don't exist
// ---------------------------------------------------------------------------

const failedPaths = new Set<string>();

// ---------------------------------------------------------------------------
// Cooldown — prevent overlapping voice lines
// ---------------------------------------------------------------------------

let lastVoiceTime = 0;
const VOICE_COOLDOWN = 800; // ms between voice lines

// ---------------------------------------------------------------------------
// Test-only helpers (named __ to discourage runtime usage)
// ---------------------------------------------------------------------------

/** Reset the fallback-warning de-dup set so tests can re-trigger warns. */
export function __resetFallbackWarnings(): void {
  fallbackWarned.clear();
}

/** Direct access to the gender-aware pool resolver for unit tests. */
export const __getGenericPool = getGenericPool;

