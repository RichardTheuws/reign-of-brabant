/**
 * Reign of Brabant -- Unit Voice Lines
 *
 * Plays faction- and unit-type-specific voice responses when units are
 * selected, moved, ordered to attack, or sent to gather resources.
 * Each action has multiple voice variants picked at random.
 *
 * Voice hierarchy: unit-type-specific → faction-generic fallback.
 * Generic fallback is gender-aware: callers may pass `gender` ('male' /
 * 'female' / 'mixed') so units with a known gender pull from the matching
 * voice-actor pool. When a female pool is empty for a faction, we warn
 * once and fall back to the male pool so audio never goes silent.
 *
 * Voice files are generated by scripts/generate_unit_voices.sh via the
 * ElevenLabs TTS API.
 *
 * Supports all 4 factions: Brabanders, Randstad, Limburgers, Belgen.
 * Supports all unit types: Worker through Hero (UnitTypeId 0-7).
 * Supports voice actions: select, move, attack, gather, death, ability, idle.
 */

