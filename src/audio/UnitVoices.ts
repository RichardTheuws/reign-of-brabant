// Public API
// ---------------------------------------------------------------------------

/**
 * Play a random voice line for the given faction, unit type, and action.
 *
 * Tries unit-type-specific voices first, falls back to faction-generic.
 * Respects a global cooldown so rapid clicks don't stack voice clips.
 * Ducks the music track while the voice plays for clarity.
 *
 * @param factionId  - 0 = Brabanders, 1 = Randstad, 2 = Limburgers, 3 = Belgen
 * @param action     - 'select' | 'move' | 'attack' | 'gather' | 'death' | 'ability' | 'idle'
 * @param unitTypeId - UnitTypeId enum value (optional, falls back to generic)
 * @param gender     - 'male' | 'female' | 'mixed' (optional, defaults to 'mixed')
 */
export function playUnitVoice(
  factionId: number,
  action: VoiceAction,
  unitTypeId?: number,
  gender: VoiceGender = 'mixed',
): void {
  const now = Date.now();
  if (now - lastVoiceTime < VOICE_COOLDOWN) return;

  // Easter-egg: 5% kans op signature line (alleen op select/idle/ready)
  if (EASTER_EGG_ACTIONS.has(action) && __easterEggRng.random() < EASTER_EGG_PROBABILITY) {
    const easterPool = EASTER_EGG_LINES[factionId]?.filter(p => !failedPaths.has(p));
    if (easterPool && easterPool.length > 0) {
      const src = easterPool[Math.floor(__easterEggRng.random() * easterPool.length)];
      const sound = new Howl({
        src: [src],
        volume: 0.7,
        onplay: () => audioManager.duckMusic(2000),
        onloaderror: () => failedPaths.add(src),
      });
      sound.play();
      lastVoiceTime = now;
      return;
    }
  }

  // Try unit-type-specific lines first
  let lines: string[] | undefined;

  if (unitTypeId !== undefined) {
    const typeName = UNIT_TYPE_NAMES[unitTypeId];
    if (typeName) {
      const unitNames = FACTION_UNIT_NAMES[factionId];
      if (unitNames) {
        const unitName = unitNames[typeName];
        const factionUnit = UNIT_VOICE_LINES[factionId];
        if (factionUnit && unitName && factionUnit[unitName]) {
          lines = factionUnit[unitName][action];
        }
      }
    }
  }

  // Filter out known-failed paths
  if (lines) {
    lines = lines.filter(p => !failedPaths.has(p));
  }

  // Fallback to gender-aware generic faction pool
  if (!lines || lines.length === 0) {
    lines = getGenericPool(factionId, action, gender).filter(p => !failedPaths.has(p));
  }

  if (!lines || lines.length === 0) return;

  // Pick a random variant
  const src = lines[Math.floor(Math.random() * lines.length)];

  const sound = new Howl({
    src: [src],
    volume: 0.6,
    onplay: () => {
      audioManager.duckMusic(1500);
    },
    onloaderror: () => {
      // Mark path as failed so we don't retry it
      failedPaths.add(src);
    },
  });

  sound.play();
  lastVoiceTime = now;
}

