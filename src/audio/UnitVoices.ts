const VOICE_BASE = '/assets/audio/voices/';

function unitLines(faction: string, unitName: string, action: VoiceAction, count: number): string[] {
  const paths: string[] = [];
  for (let i = 1; i <= count; i++) {
    paths.push(`${VOICE_BASE}${faction}/${unitName}/${action}_${i}.mp3`);
  }
  return paths;
}

function genericLines(faction: string, action: VoiceAction, count: number): string[] {
  const paths: string[] = [];
  for (let i = 1; i <= count; i++) {
    paths.push(`${VOICE_BASE}${faction}/${action}_${i}.mp3`);
  }
  return paths;
}

/**
 * Sub-pool generic lines — for additional voice actors mixed into a faction's
 * generic pool (e.g. Sharon Vlaams as 2nd female Belgen voice alongside Petra).
 * Files live in `<faction>/<sub>/<action>_N.mp3` and are appended to the
 * existing pool so the random-select picks them organically.
 */
function subPoolLines(
  faction: string,
  sub: string,
  action: VoiceAction,
  count: number,
): string[] {
  const paths: string[] = [];
  for (let i = 1; i <= count; i++) {
    paths.push(`${VOICE_BASE}${faction}/${sub}/${action}_${i}.mp3`);
  }
  return paths;
}

/**
 * Helper: generates a standard voice line set for a unit.
 * - select/move/attack: 3 variants
 * - death: 2 variants
 * - ability: 2 variants
 * - idle: 2 variants
 * - gather: reuses move lines (existing behaviour)
 */
function standardUnitVoices(
  faction: string,
  unitName: string,
): Record<VoiceAction, string[]> {
  return {
    select: unitLines(faction, unitName, 'select', 3),
    move: unitLines(faction, unitName, 'move', 3),
    attack: unitLines(faction, unitName, 'attack', 3),
    gather: unitLines(faction, unitName, 'move', 3), // reuse move for gather
    death: unitLines(faction, unitName, 'death', 2),
    ability: unitLines(faction, unitName, 'ability', 2),
    idle: unitLines(faction, unitName, 'idle', 2),
    ready: unitLines(faction, unitName, 'idle', 2), // reuse idle as spawn announcement
  };
}

