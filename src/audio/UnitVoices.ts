/**
 * Backwards-compat alias: existing call-sites and tests can keep reading
 * GENERIC_VOICE_LINES which now points at the male pool (the historical
 * default). Female callers should pass `gender: 'female'` to playUnitVoice.
 */
const GENERIC_VOICE_LINES = MALE_GENERIC_VOICE_LINES;

/** Tracks which (factionId, action) pairs we've already warned about. */
const fallbackWarned = new Set<string>();

/**
 * Resolve the generic voice-line pool for a faction + gender.
 *
 * - 'male'   → male pool
 * - 'female' → female pool, fall back to male pool with a console.warn if empty
 * - 'mixed' (or undefined) → union of male + female pools (no warn)
 */
function getGenericPool(
  factionId: number,
  action: VoiceAction,
  gender: VoiceGender,
): string[] {
  const male = MALE_GENERIC_VOICE_LINES[factionId]?.[action] ?? [];
  const female = FEMALE_GENERIC_VOICE_LINES[factionId]?.[action] ?? [];

  if (gender === 'male') return male;

  if (gender === 'female') {
    if (female.length === 0) {
      const warnKey = `${factionId}:${action}`;
      if (!fallbackWarned.has(warnKey)) {
        fallbackWarned.add(warnKey);
        console.warn(
          `[UnitVoices] Female pool empty for faction ${factionId} action "${action}" — falling back to male pool.`,
        );
      }
      return male;
    }
    return female;
  }

  // mixed: combine both, drop empties so we don't bias towards a missing pool.
  if (female.length === 0) return male;
  if (male.length === 0) return female;
  return [...male, ...female];
}

